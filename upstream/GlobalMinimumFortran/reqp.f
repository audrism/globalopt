      SUBROUTINE CALFUN(X,N,M,FF,G)
      COMMON/STATIS/IFAIL,K,ITM,L
      DIMENSION X(N),G(M)
      FF=FI(X,N)
      L=L+1
      CALL CONSTR(X,N,G,M)
      RETURN
      END
      SUBROUTINE CALGRD(X,N,G,M,A)
      COMMON /B3/F(100)/DIF/DELTA
      DIMENSION X(N),A(100,N),G(M),G1(100),G2(100)
      DO 9 I=1,M
    9 G2(I)=G(I)
      CALL CALFUN(X,N,M,F1,G)
      DO 8 I=1,M
    8 G1(I)=G(I)
      DO 2 I=1,N
      X(I)=X(I)+DELTA
      CALL CALFUN(X,N,M,F2,G)
      F(I)=(F2-F1)/DELTA
      DO 1 J=1,M
    1 A(J,I)=(G (J)-G1(J))/DELTA
    2 X(I)=X(I)-DELTA
      DO 7 I=1,M
    7 G(I)=G2(I)
      RETURN
      END
      SUBROUTINE REQP(X,B1,Q,A,N,FM,IPAR,PAR,IPA,IPAA)
      DIMENSION B1(N,N),A(100,N),Q(N,N),X(N)
      DIMENSION IPAR(30),PAR(30),G(100),U(100)
      COMMON /DIF/DELTA/FUN/FF,LG/STATIS/IFAIL,K,ITM,LF
      IFAIL=0
      LF=0
      CALL INWR35(X,G,N,IPAR,PAR,IPA,IPAA)
      IF (IFAIL.EQ.10) RETURN
      ME=IPAR(IPA+3)
      MC=IPAR(IPA+3)+IPAR(IPA+4)
      IPRINT=IPAR(IPA+1)
      IMAX=IPAR(IPA+2)
      R1=PAR(IPAA+1)
      SCALE=PAR(IPAA+2)
      DELTA=PAR(IPAA+3)
      EPS=PAR(IPAA+4)
      DO 1 I=1,N
      DO 1 J=1,N
      B1(I,J)=0.
    1 B1(I,I)=1.
      CALL RQP(N,MC,ME,X,B1,G,A,U,R1,SCALE,EPS,IPRINT,IMAX,Q)
      ITM=K
      FM=FF
      RETURN
      END
      SUBROUTINE PENFUN(X,N,M,ME,FF,FF1,G,EPS,IFEAS)
      DIMENSION X(100),G(100)
      COMMON/PEN/RSTAR
      CALL CALFUN(X,N,M,FF,G)
      IFEAS=1
      GTG=0.
      DO 1 I=1,M
      IF(G(I).LT.0.0.OR.I.LE.ME)GTG=GTG+G(I)*G(I)
      IF(I.LE.ME.AND.ABS(G(I)).GT.EPS)IFEAS=0
      IF(I.GT.ME.AND.G(I).LT.-EPS)IFEAS=0
    1 CONTINUE
      FF1=FF+GTG/RSTAR
      RETURN
      END
