      SUBROUTINE SOLVE(N1,N,Q,RHS,VAR,DIAG)
      DIMENSION RHS(100),VAR(100),DIAG(100),Q(N1,N1)
      DOUBLE PRECISION Z
      IF(N.GT.1)GO TO 21
      VAR(1)=RHS(1)/Q(1,1)
      RETURN
21    VAR(1)=RHS(1)*DIAG(1)
      DO 1 I=2,N
      Z=DBLE(RHS(I))
      II=I-1
      DO 2 L=1,II
      K=I-L
2     Z=Z-DBLE(Q(I,K)*VAR(K))
1     VAR(I)=SNGL(Z)*DIAG(I)
      VAR(N)=VAR(N)*DIAG(N)
      JJ=N-1
      DO 3 J=1,JJ
      I=N-J
      Z=DBLE(VAR(I))
      KK=I+1
      DO 4 K=KK,N
4     Z=Z-DBLE(Q(K,I)*VAR(K))
3     VAR(I)=SNGL(Z)*DIAG(I)
      RETURN
      END
