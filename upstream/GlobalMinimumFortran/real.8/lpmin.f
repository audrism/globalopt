      SUBROUTINE LPMIN(X,A,B,N,X2,NM,FMIN,IPAR,IPA)
      IMPLICIT INTEGER*4 (I - N)
      IMPLICIT REAL*8    (A - H, O - Z)
       DIMENSION X(N),A(N),B(N),IPAR(30),NR(20),X2(NM),R(20,20),X1(20),
     CX3(20),X4(20)
      COMMON /STATIS/IFAIL,IT,IM,L/INW/INW1/BS1/F(1000)
      IFAIL=0
      CALL INWR17(A,B,X,N,NM,FM,IPAR,IPA)
      IF (IFAIL.EQ.10) RETURN
      D1M=SQRT(D1MACH(2))/100.
      ISP=IPAR(IPA+1)
      M1=IPAR(IPA+2)
      M=IPAR(IPA+3)
      L=0
      IF (M1.GE.0) GO TO 1
      DO 8 I=1,N
    8 NR(I)=I
      GO TO 6
    1 IF (M1.NE.0) GO TO 2
      DO 7 I=1,N
      I1=IPA+3+I
    7 NR(I)=IPAR(I1)
      GO TO 6
    2 NM1=0
       DO 3 I=1,M1
      CALL LPTAU(FLOAT(I),N,X1)
      DO 4 J=1,N
      X3(J)=A(J)+(B(J)-A(J))*X1(J)
      NM2=NM1+J
    4 X2(NM2)=X1(J)
      F(I)=FI(X3,N)
      IF (ABS(F(I)).LT.D1M) GO TO 30
      FM=F(I)
      IFAIL=10
      CALL INWR17(A,B,X3,N,NM,FM,IPAR,IPA)
      RETURN
   30 CONTINUE
      IF (I.EQ.1) GO TO 12
      IF (F(I).GE.FMIN) GO TO 3
   12 CONTINUE
      FMIN=F(I)
      IM=I
      DO 5 J=1,N
    5 X(J)=X3(J)
    3 NM1=NM1+N
      L=M1
      CALL FAKTKK(X2,NM,F,M1,R,N,20)
      FM=-1.
      DO 13 I=1,N
      FMAX=FM
      DO 14 J=1,N
      IF(R(J,J).LE.FMAX) GO TO 14
      J1=J
      FMAX=R(J,J)
   14 CONTINUE
      NR(I)=J1
   13 R(J1,J1)=FM
      INW1=-1
      YM=FMIN
      IF (ISP.GE.0) CALL SPAU17(X,X3,N,YM,FM,NR,ISP)
      IM1=IM
    6 CONTINUE
      INW1=0
      DO 9 I=1,M
      IT=I
      CALL LPTAU(FLOAT(I),N,X1)
      DO 10 J=1,N
      I1=NR(J)
   10 X3(I1)=A(I1)+(B(I1)-A(I1))*X1(J)
      FM=FI(X3,N)
      IF (I.EQ.1) GO TO 27
      IF (FM.GE.YM) GO TO 16
   27 CONTINUE
      YM=FM
      IM=I
      DO 11 J=1,N
   11 X4(J)=X3(J)
   16 IF (ISP.LE.0) GO TO 9
      IF (MOD(I,ISP).EQ.0) CALL SPAU17(X4,X3,N,YM,FM,NR,ISP)
    9 CONTINUE
      L=L+M
      INW1=3
      IF (M1.GT.0) GO TO 25
      DO 26 I=1,N
26     X(I)=X4(I)
       FMIN=YM
      GO TO 20
   25 INW1=1
      IF (ISP.GE.0) CALL SPAU17(X4,X3,N,YM,FM,NR,ISP)
      IF (YM.GE.FMIN) GO TO 21
      IM=IM+M1
      FMIN=YM
      DO 22 I=1,N
   22 X(I)=X4(I)
      GO TO 23
   21 YM=FMIN
      IM=IM1
   23 CONTINUE
      INW1=2
   20 CONTINUE
      IF (ISP.GE.0) CALL SPAU17(X,X3,N,YM,FM,NR,ISP)
      IT=L
      FMIN=YM
      RETURN
      END
      SUBROUTINE INWR17(A,B,X,N,NM,F,IPAR,IPA)
      IMPLICIT INTEGER*4 (I - N)
      IMPLICIT REAL*8    (A - H, O - Z)
      DIMENSION A(N),B(N),IPAR(30),X(N)
      COMMON /STATIS/IFAIL,IT,IM,NF
      MN=20
      MIP=30
      MM=300
      MIM=10
      NOUT=I1MACH(2)
      IF (IFAIL.EQ.0) GO TO 4
      WRITE (NOUT,860) F
      WRITE (NOUT,580) X
      RETURN
    4 CONTINUE
      K=3
      IF (IPA.GE.0.AND.IPA+K.LE.MIP) GO TO 10
      WRITE (NOUT,520) K,MIP
      WRITE (NOUT,510) IPA
    5 IFAIL=10
      RETURN
   10 CONTINUE
      IF (IPAR(IPA+2).EQ.0) K=K+N
      IPRINT=IPAR(IPA+1)
      IF (IPRINT.LT.0) GO TO 110
      WRITE(NOUT,960)
      WRITE (NOUT,620)
      WRITE (NOUT,630) N
  110 IF (N.GT.0.AND.N.LE.MN) GO TO 20
      WRITE (NOUT,500) MN
      IF (IPRINT.LT.0) WRITE (NOUT,630) N
      GO TO 5
   20 CONTINUE
      M=IPAR(IPA+2)
      ML=IPAR(IPA+3)
      IF (IPRINT.LT.0) GO TO 40
      WRITE (NOUT,640) IPRINT
      WRITE (NOUT,650) M
      WRITE (NOUT,660) ML
   40 IF (M.LE.0.OR.(M.GE.MIM.AND.M.LE.MM)) GO TO 47
      WRITE(NOUT,530) MIM,MM
      IF (IPRINT.LT.0) WRITE (NOUT,650) M
      GO TO 5
   47 IF (NM.GE.N*M) GO TO 50
      WRITE (NOUT,730)
      IF (IPRINT.GE.0) GO TO 48
      WRITE (NOUT,630) N
      WRITE (NOUT,650) M
   48 WRITE (NOUT,635) NM
      GO TO 5
   50 CONTINUE
      IF (ML.GT.0) GO TO 60
      WRITE (NOUT,540)
      IF (IPRINT.LT.0) WRITE (NOUT,660) ML
      GO TO 5
   60 CONTINUE
      IF (M.NE.0) GO TO 140
      J=IPA+4
      L=J+N-1
      IF (IPRINT.LT.0) GO TO 70
      WRITE (NOUT,670)
      WRITE (NOUT,680) (IPAR(I),I=J,L)
   70 DO 130 I=1,N
      DO 120 L3=1,N
      J1=J+L3-1
	IF (IPAR(J1).EQ.I) GO TO 130
  120 CONTINUE
      WRITE (NOUT,690)
      IF (IPRINT.GE.0) GO TO 5
      WRITE (NOUT,670)
      WRITE (NOUT,680) (IPAR(L3),L3=J,L)
      GO TO 5
  130 CONTINUE
  140 CONTINUE
      IF (IPRINT.LT.0) GO TO 200
      WRITE (NOUT,570)
      WRITE (NOUT,580) (A(I),I=1,N)
      WRITE (NOUT,590)
      WRITE (NOUT,580) (B(I),I=1,N)
  200 NER=0
      DO 90 I=1,N
      IF (A(I).LE.B(I)) GO TO 90
      IF (NER.NE.0) GO TO 86
      WRITE (NOUT,600) I,I
      GO TO 87
   86 WRITE (NOUT,605) I,I
   87 CONTINUE
      NER=1
   90 CONTINUE
      IF (NER.NE.1) GO TO 205
      IF (IPRINT.GE.0) GO TO 5
      WRITE (NOUT,570)
      WRITE (NOUT,580) A
      WRITE (NOUT,590)
      WRITE (NOUT,580) B
      GO TO 5
  205 CONTINUE
      IF (IPRINT.LT.0) GO TO 240
      IF (M) 210,220,230
  210 WRITE (NOUT,700)
      GO TO 240
  220 WRITE (NOUT,710)
      GO TO 240
  230 WRITE (NOUT,720)
  240 RETURN
  500 FORMAT (/2X,61H**E R R O R  I N  L P M I N** N LESS THAN 1 OR N GR
     CEATER THAN,I3)
  510 FORMAT (/2X,20HNUMBER OF PARAMETERS,11X,5HIPA =,I6)
  520 FORMAT (/2X,45H**E R R O R  I N  L P M I N** IPA LESS THAN 0/2X,
     C7HOR IPA+,I2,13H GREATER THAN,I3)
  530 FORMAT (/2X,41H**E R R O R  I N  L P M I N** M LESS THAN,I3/2X,
     C17HOR M GREATER THAN,I4/)
  540 FORMAT (/2X,44H**E R R O R  I N  L P M I N** ML LESS THAN 1/)
  570 FORMAT ( /2X,32HVECTOR OF LOWER BOUNDS (A) FOR X)
  580 FORMAT (4(2X,E15.8))
  590 FORMAT (2X,32HVECTOR OF UPPER BOUNDS (B) FOR X)
  600 FORMAT (/2X,32H**E R R O R  I N  L P M I N** A(,I3,17H) GREATER TH
     CAN B(,I3,1H))
  605 FORMAT (32X,2HA(,I3,17H) GREATER THAN B(,I3,1H))
  620 FORMAT (/2X,22HI N I T I A L  D A T A)
  630 FORMAT (/2X,19HNUMBER OF VARIABLES,28X,3HN =,I6)
  635 FORMAT (2X,26HDIMENSION OF WORKING ARRAY,20X,4HNM =,I6)
  640 FORMAT (/2X,18HPRINTING PARAMETER,27X,5HIPR =,I6)
  650 FORMAT (2X,43HNUMBER OF FUNCTION EVALUATIONS FOR ANALYSIS,4X,
     C3HM =,I6)
  660 FORMAT (2X,50HNUMBER OF FUNCTION EVALUATIONS FOR LP-SEARCH  ML =,
     CI6)
  670 FORMAT (2X,33HVARIABLES BY DECREASING INFLUENCE)
  680 FORMAT (2X,20I3)
  690 FORMAT (/2X,61H**E R R O R  I N  L P M I N** INCORRECT SEQUENCE OF
     C VARIABLES/)
  700 FORMAT (/2X,9HLP-SEARCH)
  710 FORMAT (/2X,34HLP-SEARCH WITH APRIORI INFORMATION)
  720 FORMAT (/2X,23HLP-SEARCH WITH ANALYSIS)
  730 FORMAT (/2X,46H**E R R O R  I N  L P M I N** NM LESS THAN N*M)
  860 FORMAT (/2X,54H**E R R O R  I N  L P M I N** TOO LARGE FUNCTION VA
     CLUE//2X,3HF =,E15.8,2X,3HX =)
  960 FORMAT (////12X,9HL P M I N)
      END
      SUBROUTINE SPAU17(X,Z,N,YM,FF,NR,IPR)
      IMPLICIT INTEGER*4 (I - N)
      IMPLICIT REAL*8    (A - H, O - Z)
      COMMON /INW/IN/STATIS/IFAIL,IT,IM,L
      DIMENSION NR(N),X(N),Z(N)
      NOUT=I1MACH(2)
      IF (IN) 10,20,30
   10 WRITE (NOUT,100)
      WRITE (NOUT,210)
      WRITE (NOUT,220)
      WRITE (NOUT,230) (NR(I),I=1,N)
      WRITE (NOUT,150) YM,IM
      WRITE (NOUT,160)
      WRITE (NOUT,130) (X(I),I=1,N)
      RETURN
   20 CONTINUE
      WRITE(NOUT,100)
      IF (IT.EQ.IPR) WRITE (NOUT,210)
      WRITE (NOUT,110) IT
      WRITE(NOUT,120) FF
      WRITE(NOUT,140)
      WRITE(NOUT,130) (Z(I),I=1,N)
      IF (L.EQ.1) RETURN
   50 WRITE (NOUT,150) YM,IM
      WRITE(NOUT,160)
      WRITE(NOUT,130) (X(I),I=1,N)
      RETURN
   30 IF (IN.GT.1) GO TO 40
      WRITE (NOUT,100)
      WRITE (NOUT,210)
      WRITE (NOUT,240)
       GO TO 50
   40 WRITE (NOUT,170)
      WRITE(NOUT,180)
      IF (IN.EQ.3) WRITE (NOUT,150) YM,IM
	IF (IN.EQ.2) WRITE (NOUT,250) YM
      WRITE(NOUT,160)
      WRITE(NOUT,130) (X(I),I=1,N)
      WRITE(NOUT,190) L
      WRITE (NOUT,200)
      WRITE(NOUT,170)
      RETURN
  100 FORMAT (/1X,34(2H -))
  110 FORMAT(2X,23HI T E R A T I O N  NR =,I6)
  120 FORMAT (/2X,19HFUNCTION VALUE F = ,E15.8)
  130 FORMAT(4(2X,E15.8))
  140 FORMAT (2X,13HCURRENT POINT)
  150 FORMAT (/2X,28HOPTIMAL FUNCTION VALUE FM = ,E15.8,17H OBTAINED IN
     CNR =,I6)
  160 FORMAT(2X,13HOPTIMAL POINT)
  170 FORMAT (/1X,34(2H *)/1X,34(2H *))
  180 FORMAT (/2X,13HR E S U L T S)
  190 FORMAT (/2X,34HNUMBER OF FUNCTION EVALUATIONS L =,I6)
  200 FORMAT (/7X,16HLPMIN TERMINATED)
  210 FORMAT (1X,34(2H -))
  220 FORMAT (/2X,19HRESULTS OF ANALYSIS//2X,
     C33HVARIABLES BY DECREASING INFLUENCE)
  230 FORMAT (2X,20I3)
  240 FORMAT (/2X,20HRESULTS OF LP-SEARCH)
  250 FORMAT (/2X,28HOPTIMAL FUNCTION VALUE FM = ,E15.8)
      END
      SUBROUTINE FAKTKK(X,NM,F,M,R,N,MR)
      IMPLICIT INTEGER*4 (I - N)
      IMPLICIT REAL*8    (A - H, O - Z)
      DIMENSION X(NM),F(M),R(MR,MR)
      RL=FLOAT(M)
      RL=RL*RL-RL
      DO 3 I=1,N
      DO 3 J=I,N
    3 R(I,J)=0.
      M1=M-1
      NM1=0
      DO 11 K=1,M1
      K2=K+1
      NM2=NM1+N
      DO 12 K1=K2,M
      Z=0.
      DO 5 JJ=1,N
      NM3=NM1+JJ
      NM4=NM2+JJ
    5 Z=Z+(X(NM3)-X(NM4))**2
      IF (Z.LT.1.E-10) GO TO 12
      FK=(F(K)-F(K1))**2
      DO 33 I=1,N
      NM3=NM1+I
      NM4=NM2+I
      DO 33 J=I,N
      NM5=NM1+J
      NM6=NM2+J
   33 R(I,J)=R(I,J)+FK*(X(NM3)-X(NM4))*(X(NM5)-X(NM6))/Z
   12 NM2=NM2+N
      NM1=NM1+N
   11 CONTINUE
      DO 34 I=1,N
      DO 34 J=I,N
      R(I,J)=R(I,J)/RL
      IF(I.EQ.J) GO TO 34
      R(J,I)=R(I,J)
   34 CONTINUE
      RETURN
      END

