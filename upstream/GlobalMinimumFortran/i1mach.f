      FUNCTION FUBRAN(X,N)
C     BRANIN FUNCTION
      DIMENSION X(N)
      X1 = X(1)
      X2 = X(2)
      FUBRAN=(X2-0.1292*X1*X1+1.59155*X1-6.)**2+9.60211*COS(X1)+10.
      RETURN
      END
      FUNCTION FUGOLD(X,N)
C     GOLDSTEIN AND PRICE FUNCTION
      DIMENSION X(N)
      X1=X(1)
      X2=X(2)
      X3=X1*X1
      X4=X2*X2
      X5=X1*X2
