      FUNCTION ATS(J)
      COMMON /ATT/X(15)
      X1=X(1)+X(15)
      IF (X1.GT.1.) X1=X1-1.
      DO 1 I=2,15
    1 X(I-1)=X(I)
      X(15)=X1
      ATS=X1
      RETURN
      END
