      SUBROUTINE START
      COMMON /U/NX,X(20)/V/STEP,K1,X1(29,20)
      DIMENSION A(21,20)
      VN=FLOAT(NX)
      STEP1=STEP/(VN*SQRT(2.))*(SQRT(VN+1.)+VN-1.)
      STEP2=STEP/(VN*SQRT(2.))*(SQRT(VN+1.)-1.)
      DO 1 J=1,NX
    1 A(1,J)=0.
      DO 2 I=2,K1
      DO 4 J=1,NX
    4 A(I,J)=STEP2
      L=I-1
      A(I,L)=STEP1
    2 CONTINUE
      DO 3 I=1,K1
      DO 3 J=1,NX
    3 X1(I,J)=X(J)+A(I,J)
      RETURN
      END
      SUBROUTINE SUMR
C*****THIS SUBROUTINE COMPUTES THE SUM OF THE SQUARE VALUES OF THE
C     VIOLATED CONSTRAINTS IN ORDER TO BE COMPARED WITH THE TOLERANCE
C     CRITERION
      COMMON /U/NX,X(20)/C/NC,NIC,R(100)/D/K6,
     1K7,K9/E/IN,K8,SEQL,SUM(29)
      SUM(IN)=0.
      SEQL=0.
      IF (K8.EQ.0) GO TO 3
      CALL CONSTR(X,NX,R,K8)
      IF(NIC.EQ.0) GO TO 4
      DO 1 J=K7,K8
      IF(R(J).GE.0.) GO TO 1
      SEQL=SEQL+R(J)*R(J)
    1 CONTINUE
    4 IF(NC.EQ.0) GO TO 3
      DO 2 J=1,NC
    2 SEQL=SEQL+R(J)*R(J)
    3 SUM(IN)=SEQL
    5 RETURN
      END
