use crate::optimizers::OptError;

#[derive(Debug, Clone)]
pub struct AtsGenerator {
    state: [f64; 15],
}

impl Default for AtsGenerator {
    fn default() -> Self {
        Self {
            state: [
                0.86515, 0.90795, 0.66155, 0.66434, 0.56558, 0.12332, 0.69186, 0.03393,
                0.42502, 0.99224, 0.88955, 0.53758, 0.41686, 0.42163, 0.85181,
            ],
        }
    }
}

impl AtsGenerator {
    pub fn next(&mut self) -> f64 {
        let mut x1 = self.state[0] + self.state[14];
        if x1 > 1.0 {
            x1 -= 1.0;
        }
        for i in 1..15 {
            self.state[i - 1] = self.state[i];
        }
        self.state[14] = x1;
        x1
    }
}

pub fn lp_tau_point(c: usize, n: usize) -> Result<Vec<f64>, OptError> {
    if c == 0 {
        return Err(OptError::InvalidInput(
            "LPTAU point index must start at 1".to_string(),
        ));
    }
    if n == 0 || n > 20 {
        return Err(OptError::InvalidInput(
            "LPTAU dimension must be in 1..=20".to_string(),
        ));
    }

    let c_f = c as f64;
    let m = 1 + (c_f.ln() / 0.693_147).floor() as usize;
    let mut x = vec![0.0; n];

    for j in 1..=n {
        let mut s = 0.0;
        for k in 1..=m {
            let mut ns = 0_i32;
            for l in k..=m {
                let nn = (l - 1) * 20 + j;
                let b = nr(nn)?;
                let left = (2.0 * frac(c_f / 2_f64.powi(l as i32))).floor() as i32;
                let right = (2.0 * frac(b / 2_f64.powi((l + 1 - k) as i32))).floor() as i32;
                ns += left * right;
            }
            let rs = ns as f64;
            s += frac(0.5 * rs) / 2_f64.powi((k - 1) as i32);
        }
        x[j - 1] = s;
    }

    Ok(x)
}

fn frac(v: f64) -> f64 {
    v - v.floor()
}

fn nr(i: usize) -> Result<f64, OptError> {
    if i == 0 || i > 400 {
        return Err(OptError::Internal(format!(
            "LPTAU constant index out of range: {i}"
        )));
    }

    if i <= 100 {
        return Ok(A1[i - 1]);
    }
    if i <= 200 {
        return Ok(A2[i - 101]);
    }
    if i <= 280 {
        return Ok(A3[i - 201]);
    }
    if i <= 340 {
        return Ok(A4[i - 281]);
    }

    Ok(A5[i - 341])
}

